/**
 * Defines colors using the Red Green Blue Alpha model (RGBA)
 * @property r - Red values should be between 0 and 255
 * @property g - Green values should be between 0 and 255
 * @property b - Blue values should be between 0 and 255
 * @property a - Alpha values should be between 0 and 1
 * */
export interface RGBAColor {
    r: number
    g: number
    b: number
    a: number
}
